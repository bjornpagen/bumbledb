/// Facts per write transaction in [`Db::bulk_load`] — the documented
/// chunking convenience (`40-storage.md`): large imports amortize commit
/// overhead without building one unbounded delta.
const BULK_CHUNK: usize = 4096;

/// One typed fact struct, as generated by the `schema!` macro. The write
/// side encodes against the delta (interning novel strings/bytes); the
/// read side encodes against the committed dictionary and reports a
/// never-interned value as "this fact cannot exist".
pub trait Fact: Sized {
    /// The relation this struct declares, by declaration order.
    const RELATION: RelationId;

    /// Encodes the canonical fact bytes against a write context, interning
    /// novel strings and bytes through the delta. Appends to `out`.
    ///
    /// # Errors
    ///
    /// Storage errors from the dictionary reads.
    fn encode_write(&self, tx: &mut WriteTx<'_>, out: &mut Vec<u8>) -> Result<()>;

    /// Encodes for the delete path: pending intern ids first (so an
    /// insert-then-delete within one transaction cancels byte-exactly),
    /// then the committed dictionary — never minting. `Ok(false)` means a
    /// string or bytes value is known to neither: the fact provably
    /// cannot exist in base or delta, the delete is a no-op, and the
    /// dictionary stays untouched (`out` is left unusable in that case).
    ///
    /// # Errors
    ///
    /// Storage errors from the dictionary reads.
    fn encode_delete(&self, tx: &WriteTx<'_>, out: &mut Vec<u8>) -> Result<bool>;

    /// Encodes against a read context. `Ok(false)` means a string or bytes
    /// value was never interned — the fact cannot exist in the database
    /// (`out` is left untouched in that case; otherwise appended).
    ///
    /// # Errors
    ///
    /// Storage errors from the dictionary reads.
    fn encode_read(&self, snap: &Snapshot<'_>, out: &mut Vec<u8>) -> Result<bool>;

    /// Decodes canonical fact bytes back into the typed struct, resolving
    /// intern ids through the snapshot's dictionary.
    ///
    /// # Errors
    ///
    /// `Corruption` on undecodable bytes or dangling intern ids.
    fn decode(snap: &Snapshot<'_>, fact: &[u8]) -> Result<Self>;

    /// Decodes canonical fact bytes inside a write transaction — the
    /// point-read sibling of [`Fact::decode`], resolving intern ids
    /// pending-first (a fact inserted this transaction carries provisional
    /// ids) and through the committed dictionary otherwise.
    ///
    /// # Errors
    ///
    /// `Corruption` on undecodable bytes or dangling intern ids.
    fn decode_write(tx: &WriteTx<'_>, fact: &[u8]) -> Result<Self>;
}

/// Generated alongside [`Fact`] for relations with **exactly one** serial
/// field: names the newtype [`WriteTx::get`] accepts as the key. Relations
/// with zero or several key statements have no single dominant key, so
/// their point reads spell the statement out through [`WriteTx::get_dyn`]
/// (the multi-key typed shape is an open item, `docs/architecture/70-api.md`).
pub trait SerialKeyed: Fact {
    /// The serial field's generated newtype.
    type SerialKey: Serial;
}

/// A serial-minted newtype, as generated by the `schema!` macro for a
/// `serial` field declared `as NewType`: [`WriteTx::alloc`] mints the next
/// value with the field already known.
pub trait Serial: Sized {
    /// The relation owning the serial field.
    const RELATION: RelationId;
    /// The serial field itself.
    const FIELD: FieldId;
    /// Wraps a minted raw value.
    fn from_serial(raw: u64) -> Self;
    /// The raw value back out — the encode side of [`WriteTx::get`]'s
    /// key lookup (serial newtypes are `Copy`).
    fn serial(self) -> u64;
}

/// The database handle: the LMDB environment, the image cache, and the
/// writer mutex. Shareable across threads (`Send + Sync`); dropping the
/// handle closes the environment.
///
/// `'s` borrows the schema. The `schema!` macro's generated `schema()`
/// returns `&'static Schema` (a `OnceLock`), so macro-declared hosts get
/// `Db<'static>` and cross thread bounds freely; a hand-built
/// [`crate::schema::SchemaDescriptor`] schema can be promoted with
/// `Box::leak` when `'static` is needed.
///
/// Transaction closures return [`crate::Result`]; host code with its own
/// error type wraps the transaction instead of threading the type
/// through: run the closure for the engine work, then convert — a
/// generic error parameter here would force turbofish annotations on
/// every plain `Ok(())` closure.
pub struct Db<'s> {
    env: Environment,
    cache: ImageCache,
    writer: Mutex<()>,
    /// The thread currently inside [`Db::write`] (0 = none): a nested
    /// `write` on the same thread would self-deadlock on the writer
    /// mutex forever, so it panics loudly instead.
    writer_thread: std::sync::atomic::AtomicU64,
    /// The reader cache (docs/silicon/12): one parked LMDB read
    /// transaction, reused while no commit has intervened. Sound because
    /// this handle is the environment's ONLY writer (exclusive lock at
    /// open): if [`Db::commit_seq`] is unchanged since the parked
    /// snapshot began, the parked snapshot is bit-identical to a fresh
    /// one — and the per-read `mdb_txn_begin` (the point path's last
    /// fixed cost, docs/perf PRD 11) is skipped entirely. Readers
    /// `try_lock`: contended readers fall back to a fresh transaction,
    /// never block.
    read_cache: Mutex<Option<ParkedReader>>,
    /// Commits since open (monotone; bumped only when a commit changed
    /// state). The parked reader's validity token.
    commit_seq: std::sync::atomic::AtomicU64,
    schema: &'s Schema,
}

/// One parked read transaction and the commit sequence it saw.
struct ParkedReader {
    txn: heed::RoTxn<'static, heed::WithoutTls>,
    commit_seq: u64,
}

/// Clears the owner mark when the write closure exits — normally, by
/// error, or by unwind — so the next `write` on this thread proceeds.
struct WriterThreadReset<'a>(&'a std::sync::atomic::AtomicU64);

/// A failed [`Db::bulk_load`]: the underlying error plus the number of
/// facts committed by the complete chunks before it — every fact of a
/// committed chunk is durable; nothing of the failing chunk is.
#[derive(Debug)]
pub struct BulkLoadError {
    /// Facts that changed state in the chunks committed before the error.
    pub committed: u64,
    /// What stopped the import.
    pub error: crate::error::Error,
}

/// One consistent read snapshot: executes prepared queries and exports
/// relations. Handed to [`Db::read`] closures.
pub struct Snapshot<'db> {
    txn: ReadTxn<'db>,
    cache: &'db ImageCache,
    schema: &'db Schema,
}

/// One write transaction: an in-memory write delta over a read view. Operations
/// are in-memory set arithmetic — order is semantically irrelevant, and
/// `delete(old); insert(new)` in either order is the blessed mutation
/// idiom. Handed to [`Db::write`] closures; offers no queries — point
/// reads only ([`WriteTx::contains`] / [`WriteTx::get`] /
/// [`WriteTx::get_dyn`]), which observe the final-state view the judgment
/// phase will judge. No prepared-query or snapshot type is reachable from
/// here (`docs/architecture/70-api.md`: full queries in write transactions
/// stay unrepresentable).
pub struct WriteTx<'a> {
    view: ReadTxn<'a>,
    delta: WriteDelta<'a>,
    schema: &'a Schema,
    scratch: Vec<u8>,
    /// Reused per dynamic fact: `bulk_load` must not allocate a value
    /// buffer per imported row.
    refs: Vec<ValueRef>,
}

/// How [`WriteTx::encode_dyn`] treats novel strings and bytes: the insert
/// path mints provisional intern ids; the delete path only resolves —
/// a value neither pending nor committed proves its fact absent.
#[derive(Clone, Copy, PartialEq, Eq)]
enum InternMode {
    Mint,
    Resolve,
}

