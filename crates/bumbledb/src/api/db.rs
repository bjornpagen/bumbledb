/// One consistent read snapshot: executes prepared queries and exports
/// relations. Handed to [`Db::read`] closures. Carries the handle's
/// schema typestate `S`, so a prepared query executes only against
/// snapshots of a same-schema database by construction.
pub struct Snapshot<'db, S> {
    txn: ReadTxn<'db>,
    cache: &'db ImageCache,
    schema: &'db Schema,
    marker: PhantomData<fn() -> S>,
}

impl<'db, S> Snapshot<'db, S> {
    /// The snapshot's read transaction (reader: the staleness signal —
    /// [`crate::PreparedQuery::staleness`] takes the snapshot directly
    /// rather than routing through a `Snapshot` wrapper method).
    pub(crate) fn txn(&self) -> &ReadTxn<'db> {
        &self.txn
    }
}

/// One write transaction: an in-memory write delta over a read view. Operations
/// are in-memory set arithmetic — order is semantically irrelevant, and
/// `delete(old); insert(new)` in either order is the blessed mutation
/// idiom. Handed to [`Db::write`] closures; offers no queries — point
/// reads only ([`WriteTx::contains`] / [`WriteTx::get`] /
/// [`WriteTx::get_dyn`]), which observe the final-state view the judgment
/// phase will judge. No prepared-query or snapshot type is reachable from
/// here (`docs/architecture/70-api.md`: full queries in write transactions
/// stay unrepresentable). Carries the handle's schema typestate `S`:
/// typed operations bound `F: Fact<'_, Schema = S>`.
pub struct WriteTx<'a, S> {
    view: ReadTxn<'a>,
    delta: WriteDelta<'a>,
    schema: &'a Schema,
    scratch: Vec<u8>,
    /// Reused per dynamic fact: `bulk_load` must not allocate a value
    /// buffer per imported row.
    refs: Vec<ValueRef>,
    marker: PhantomData<fn() -> S>,
}

impl<S> WriteTx<'_, S> {
    /// The closed-relation write refusal
    /// (`docs/architecture/10-data-model.md` § closed relations): rows of
    /// a closed relation are ground axioms — changing them is a new theory
    /// (fingerprint), never a delta — so every write-surface entry types
    /// the attempt away before any encoding runs. Total over
    /// data-supplied ids: an unknown id passes through to the dynamic
    /// surface's own shape check.
    fn refuse_closed(&self, relation: RelationId) -> Result<()> {
        match self.schema.relation_checked(relation) {
            Some(rel) if rel.is_closed() => {
                Err(crate::error::Error::ClosedRelationWrite { relation })
            }
            _ => Ok(()),
        }
    }

    /// Runs `body` with the encode scratch taken out (a typed encode
    /// borrows the whole transaction mutably alongside the buffer),
    /// restoring the buffer — and its capacity — afterward, success or
    /// error. The one scratch discipline of every typed operation.
    fn with_scratch<R>(
        &mut self,
        body: impl FnOnce(&mut Self, &mut Vec<u8>) -> Result<R>,
    ) -> Result<R> {
        let mut bytes = std::mem::take(&mut self.scratch);
        bytes.clear();
        let out = body(self, &mut bytes);
        self.scratch = bytes;
        out
    }
}

/// How [`WriteTx::encode_dyn`] treats novel strings and bytes: the insert
/// path mints provisional intern ids; the delete path only resolves —
/// a value neither pending nor committed proves its fact absent.
#[derive(Clone, Copy, PartialEq, Eq)]
enum InternMode {
    Mint,
    Resolve,
}

