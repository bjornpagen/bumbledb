//! The embedding surface (PRD 28, `docs/architecture/60-api.md`): [`Db`],
//! the read/write transaction closures, the typed write path, and export.
//!
//! Threading doctrine (`00-product.md`): the engine owns zero threads. The
//! handle is `Send + Sync`; readers run concurrently on LMDB snapshots;
//! writes serialize on one mutex. A write transaction is a [`WriteDelta`]
//! — in-memory set arithmetic, nothing touches LMDB until commit, and an
//! abort (error or panic) never wrote anything.

use std::path::Path;
use std::sync::{Mutex, PoisonError};

use crate::encoding::{decode_field, encode_fact, ValueRef};
use crate::error::{FactShapeError, Result};
use crate::image::cache::ImageCache;
use crate::ir::{Query, Value};
use crate::schema::{FieldId, RelationId, Schema};
use crate::storage::commit::commit;
use crate::storage::delta::WriteDelta;
use crate::storage::dict;
use crate::storage::env::{Environment, ReadTxn};
use crate::storage::read;

use super::prepared::{prepare, PreparedQuery, ResultBuffer};

/// Facts per write transaction in [`Db::bulk_load`] — the documented
/// chunking convenience (`40-storage.md`): large imports amortize commit
/// overhead without building one unbounded delta.
const BULK_CHUNK: usize = 4096;

/// One typed fact struct, as generated by the `schema!` macro. The write
/// side encodes against the delta (interning novel strings/bytes); the
/// read side encodes against the committed dictionary and reports a
/// never-interned value as "this fact cannot exist".
pub trait Fact: Sized {
    /// The relation this struct declares, by declaration order.
    const RELATION: RelationId;

    /// Encodes the canonical fact bytes against a write context, interning
    /// novel strings and bytes through the delta. Appends to `out`.
    ///
    /// # Errors
    ///
    /// Storage errors from the dictionary reads.
    fn encode_write(&self, tx: &mut WriteTx<'_>, out: &mut Vec<u8>) -> Result<()>;

    /// Encodes against a read context. `Ok(false)` means a string or bytes
    /// value was never interned — the fact cannot exist in the database
    /// (`out` is left untouched in that case; otherwise appended).
    ///
    /// # Errors
    ///
    /// Storage errors from the dictionary reads.
    fn encode_read(&self, snap: &Snapshot<'_>, out: &mut Vec<u8>) -> Result<bool>;

    /// Decodes canonical fact bytes back into the typed struct, resolving
    /// intern ids through the snapshot's dictionary.
    ///
    /// # Errors
    ///
    /// `Corruption` on undecodable bytes or dangling intern ids.
    fn decode(snap: &Snapshot<'_>, fact: &[u8]) -> Result<Self>;
}

/// A serial-minted newtype, as generated by the `schema!` macro for a
/// `serial` field declared `as NewType`: [`WriteTx::alloc`] mints the next
/// value with the field already known.
pub trait Serial: Sized {
    /// The relation owning the serial field.
    const RELATION: RelationId;
    /// The serial field itself.
    const FIELD: FieldId;
    /// Wraps a minted raw value.
    fn from_serial(raw: u64) -> Self;
}

/// The database handle: the LMDB environment, the image cache, and the
/// writer mutex. Shareable across threads (`Send + Sync`); dropping the
/// handle closes the environment.
///
/// `'s` borrows the schema. The `schema!` macro's generated `schema()`
/// returns `&'static Schema` (a `OnceLock`), so macro-declared hosts get
/// `Db<'static>` and cross thread bounds freely; a hand-built
/// [`crate::schema::SchemaDescriptor`] schema can be promoted with
/// `Box::leak` when `'static` is needed.
///
/// Transaction closures return [`crate::Result`]; host code with its own
/// error type wraps the transaction instead of threading the type
/// through: run the closure for the engine work, then convert — a
/// generic error parameter here would force turbofish annotations on
/// every plain `Ok(())` closure.
pub struct Db<'s> {
    env: Environment,
    cache: ImageCache,
    writer: Mutex<()>,
    schema: &'s Schema,
}

impl<'s> Db<'s> {
    /// Initializes a fresh environment at `path` with the schema's
    /// fingerprint and opens it.
    ///
    /// # Errors
    ///
    /// `Io`/`Lmdb` on environment creation failure.
    pub fn create(path: &Path, schema: &'s Schema) -> Result<Self> {
        Ok(Self::assemble(Environment::create(path, schema)?, schema))
    }

    /// Opens an existing environment, verifying the format version and
    /// then the schema fingerprint — each mismatch is a typed hard
    /// failure. Production open never destroys data.
    ///
    /// # Errors
    ///
    /// `FormatMismatch`/`SchemaMismatch` on verification failure;
    /// `Io`/`Lmdb` otherwise.
    pub fn open(path: &Path, schema: &'s Schema) -> Result<Self> {
        Ok(Self::assemble(Environment::open(path, schema)?, schema))
    }

    fn assemble(env: Environment, schema: &'s Schema) -> Self {
        Self {
            env,
            cache: ImageCache::new(),
            writer: Mutex::new(()),
            schema,
        }
    }

    /// Runs `f` over one LMDB read snapshot: a consistent generation for
    /// every query and scan inside.
    ///
    /// # Errors
    ///
    /// `Lmdb` on snapshot open; otherwise whatever `f` returns.
    pub fn read<R>(&self, f: impl FnOnce(&Snapshot<'_>) -> Result<R>) -> Result<R> {
        let snap = Snapshot {
            txn: self.env.read_txn()?,
            cache: &self.cache,
            schema: self.schema,
        };
        f(&snap)
    }

    /// Runs `f` as the single writer: takes the writer mutex, hands `f` a
    /// delta transaction, and commits on `Ok`. `Err` or panic drops the
    /// delta — LMDB was never touched. Constraints are checked at commit
    /// against the final state; a violation aborts the whole transaction.
    ///
    /// Queries are not reachable from the write closure — [`WriteTx`]
    /// simply offers none (forbidden by representation, `60-api.md`):
    /// read-modify-write is a read transaction followed by a write
    /// transaction.
    ///
    /// # Errors
    ///
    /// `f`'s error, or commit-time `UniqueViolation` /
    /// `ForeignKeyViolation` / `SerialExhausted` / `Lmdb` / `Io`.
    pub fn write<R>(&self, f: impl FnOnce(&mut WriteTx<'_>) -> Result<R>) -> Result<R> {
        // A panicking closure poisons nothing real: the delta died in the
        // unwind and LMDB was never touched, so the flag is cleared rather
        // than propagated.
        let _guard = self.writer.lock().unwrap_or_else(PoisonError::into_inner);
        let mut tx = WriteTx {
            view: self.env.read_txn()?,
            delta: WriteDelta::new(self.schema),
            schema: self.schema,
            scratch: Vec::new(),
            refs: Vec::new(),
        };
        let out = f(&mut tx)?;
        let WriteTx { view, delta, .. } = tx;
        drop(view);
        let report = commit(delta, &self.env)?;
        if report.changed {
            // The one commit → cache wiring point (`40-storage.md`):
            // images of older generations are stale the moment the new
            // generation exists.
            self.cache.evict_older_than(report.new_generation);
        }
        Ok(out)
    }

    /// Prepares a query against current statistics (pin-at-prepare). The
    /// prepared query outlives the internal snapshot and is reusable
    /// across [`Db::read`] closures.
    ///
    /// # Errors
    ///
    /// The PRD 14 [`crate::error::ValidationError`] roster, at prepare
    /// time; `Lmdb` from the statistics reads.
    pub fn prepare(&self, query: &Query) -> Result<PreparedQuery<'s>> {
        let txn = self.env.read_txn()?;
        prepare(&txn, &self.cache, self.schema, query)
    }

    /// Imports dynamic facts in chunks of 4096 per write
    /// transaction — the same delta mechanism at scale. Explicit serial
    /// values preserve identity: the high-water mark advances past them.
    /// Returns the number of facts that changed state.
    ///
    /// A fresh-database append-order fast path is a documented possibility
    /// (`40-storage.md`) deliberately not taken: it saves only the
    /// membership probes on an empty database, and the normal insert path
    /// is the one with the invariants (decision: do not gold-plate).
    ///
    /// # Errors
    ///
    /// [`BulkLoadError`]: the underlying error plus how many facts had
    /// already committed — a failing chunk aborts that chunk whole,
    /// leaving prior chunks committed, and the count makes the partial
    /// import sizable and resumable. Shape problems are typed `FactShape`
    /// errors, as [`WriteTx::insert_dyn`].
    pub fn bulk_load<I>(&self, rel: RelationId, facts: I) -> std::result::Result<u64, BulkLoadError>
    where
        I: IntoIterator<Item = Vec<Value>>,
    {
        let mut iter = facts.into_iter();
        let mut total = 0u64;
        loop {
            let mut exhausted = false;
            self.write(|tx| {
                for _ in 0..BULK_CHUNK {
                    let Some(values) = iter.next() else {
                        exhausted = true;
                        break;
                    };
                    if tx.insert_dyn(rel, &values)? {
                        total += 1;
                    }
                }
                Ok(())
            })
            .map_err(|error| BulkLoadError {
                committed: total,
                error,
            })?;
            if exhausted {
                return Ok(total);
            }
        }
    }
}

/// A failed [`Db::bulk_load`]: the underlying error plus the number of
/// facts committed by the complete chunks before it — every fact of a
/// committed chunk is durable; nothing of the failing chunk is.
#[derive(Debug)]
pub struct BulkLoadError {
    /// Facts that changed state in the chunks committed before the error.
    pub committed: u64,
    /// What stopped the import.
    pub error: crate::error::Error,
}

impl std::fmt::Display for BulkLoadError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "bulk load failed after {} committed facts: {}",
            self.committed, self.error
        )
    }
}

impl std::error::Error for BulkLoadError {
    fn source(&self) -> Option<&(dyn std::error::Error + 'static)> {
        Some(&self.error)
    }
}

/// Dropping the count keeps `?` working in `crate::Result` contexts.
impl From<BulkLoadError> for crate::error::Error {
    fn from(err: BulkLoadError) -> Self {
        err.error
    }
}

/// One consistent read snapshot: executes prepared queries and exports
/// relations. Handed to [`Db::read`] closures.
pub struct Snapshot<'db> {
    txn: ReadTxn<'db>,
    cache: &'db ImageCache,
    schema: &'db Schema,
}

impl Snapshot<'_> {
    /// Executes a prepared query with positional parameters into the
    /// caller's reusable buffer (the zero-alloc path).
    ///
    /// # Errors
    ///
    /// `ParamCountMismatch`/`ParamTypeMismatch` at bind time; `Overflow`
    /// from aggregate finalization; `Lmdb`/`Corruption` from storage. A
    /// query error aborts the query; the snapshot remains usable.
    pub fn execute(
        &self,
        prepared: &mut PreparedQuery<'_>,
        params: &[Value],
        out: &mut ResultBuffer,
    ) -> Result<()> {
        prepared.execute(&self.txn, self.cache, params, out)
    }

    /// Convenience path: a fresh buffer per call.
    ///
    /// # Errors
    ///
    /// As [`Snapshot::execute`].
    pub fn execute_collect(
        &self,
        prepared: &mut PreparedQuery<'_>,
        params: &[Value],
    ) -> Result<ResultBuffer> {
        prepared.execute_collect(&self.txn, self.cache, params)
    }

    /// EXPLAIN ANALYZE (PRD 24): executes with counting instrumentation
    /// and returns the rows alongside the rendered report.
    ///
    /// # Errors
    ///
    /// As [`Snapshot::execute`].
    pub fn explain(
        &self,
        prepared: &mut PreparedQuery<'_>,
        params: &[Value],
    ) -> Result<(ResultBuffer, String)> {
        prepared.explain(&self.txn, self.cache, params)
    }

    /// The export surface (`60-api.md` ETL story): a full-relation scan
    /// yielding decoded dynamic facts (strings and bytes resolved) in
    /// `row_id` order — a storage stream, not a query result set.
    ///
    /// # Errors
    ///
    /// `Lmdb` on cursor open; per-item `Corruption` is a hard error — stop
    /// at the first.
    pub fn scan(&self, rel: RelationId) -> Result<impl Iterator<Item = Result<Vec<Value>>> + '_> {
        let layout = self.schema.relation(rel).layout();
        let iter = read::scan(&self.txn, self.schema, rel)?;
        Ok(iter.map(move |entry| {
            let (_, bytes) = entry?;
            (0..layout.field_count())
                .map(|idx| {
                    Ok(match decode_field(bytes, layout, idx)? {
                        ValueRef::Bool(v) => Value::Bool(v),
                        ValueRef::U64(v) => Value::U64(v),
                        ValueRef::I64(v) => Value::I64(v),
                        ValueRef::Enum(ordinal) => Value::Enum(ordinal),
                        ValueRef::String(id) => Value::String(Box::from(dict::resolve(
                            &self.txn,
                            id,
                            dict::TAG_STRING,
                        )?)),
                        ValueRef::Bytes(id) => {
                            Value::Bytes(Box::from(dict::resolve(&self.txn, id, dict::TAG_BYTES)?))
                        }
                    })
                })
                .collect()
        }))
    }
}

impl Snapshot<'_> {
    /// The typed sibling of [`Snapshot::scan`]: decodes each fact into its
    /// `schema!`-generated struct via [`Fact::decode`]. The dynamic form
    /// remains the ETL pairing for [`Db::bulk_load`]; this one is for
    /// hosts that want their own types back.
    ///
    /// # Errors
    ///
    /// As [`Snapshot::scan`].
    pub fn scan_facts<F: Fact>(&self) -> Result<impl Iterator<Item = Result<F>> + '_> {
        let iter = read::scan(&self.txn, self.schema, F::RELATION)?;
        Ok(iter.map(move |entry| {
            let (_, bytes) = entry?;
            F::decode(self, bytes)
        }))
    }
}

/// One write transaction: an in-memory write delta over a read view. Operations
/// are in-memory set arithmetic — order is semantically irrelevant, and
/// `delete(old); insert(new)` in either order is the blessed mutation
/// idiom. Handed to [`Db::write`] closures; offers no queries.
pub struct WriteTx<'a> {
    view: ReadTxn<'a>,
    delta: WriteDelta<'a>,
    schema: &'a Schema,
    scratch: Vec<u8>,
    /// Reused per dynamic fact: `bulk_load` must not allocate a value
    /// buffer per imported row.
    refs: Vec<ValueRef>,
}

impl WriteTx<'_> {
    /// Mints the next serial value for the newtype's field — insert new
    /// rows without reading a max (`10-data-model.md`).
    ///
    /// # Errors
    ///
    /// `SerialExhausted` at `u64::MAX`; `Lmdb` on the sequence read.
    pub fn alloc<T: Serial>(&mut self) -> Result<T> {
        self.delta
            .alloc(&self.view, T::RELATION, T::FIELD)
            .map(T::from_serial)
    }

    /// Untyped serial minting for ETL tooling.
    ///
    /// # Errors
    ///
    /// As [`WriteTx::alloc`].
    ///
    /// # Panics
    ///
    /// If `field` is not `Serial` generation — the untyped path is the
    /// caller's responsibility to point at a serial field.
    pub fn alloc_dyn(&mut self, rel: RelationId, field: FieldId) -> Result<u64> {
        self.delta.alloc(&self.view, rel, field)
    }

    /// Records a typed insert. Returns whether the final state changes (an
    /// idempotent no-op if the fact is already present).
    ///
    /// # Errors
    ///
    /// `Lmdb` on the membership probe or dictionary reads.
    pub fn insert<F: Fact>(&mut self, fact: &F) -> Result<bool> {
        let mut bytes = std::mem::take(&mut self.scratch);
        bytes.clear();
        let changed = fact
            .encode_write(self, &mut bytes)
            .and_then(|()| self.delta.insert(&self.view, F::RELATION, &bytes));
        self.scratch = bytes;
        changed
    }

    /// Records a typed delete. Returns whether the final state changes (an
    /// idempotent no-op if the fact is absent).
    ///
    /// Encodes through the *write* context deliberately: a fact inserted
    /// and deleted within one transaction must cancel exactly, so the
    /// delete must see the same provisional intern ids the insert minted.
    ///
    /// # Errors
    ///
    /// As [`WriteTx::insert`].
    pub fn delete<F: Fact>(&mut self, fact: &F) -> Result<bool> {
        let mut bytes = std::mem::take(&mut self.scratch);
        bytes.clear();
        let changed = fact
            .encode_write(self, &mut bytes)
            .and_then(|()| self.delta.delete(&self.view, F::RELATION, &bytes));
        self.scratch = bytes;
        changed
    }

    /// Records a dynamic insert (the ETL form): one [`Value`] per field in
    /// declaration order.
    ///
    /// # Errors
    ///
    /// `FactShape` on an arity/type/enum-range/UTF-8 mismatch between
    /// `values` and the relation's declaration (ETL input is data, so
    /// shape problems are typed); otherwise as [`WriteTx::insert`].
    pub fn insert_dyn(&mut self, rel: RelationId, values: &[Value]) -> Result<bool> {
        self.encode_dyn(rel, values)?;
        self.delta.insert(&self.view, rel, &self.scratch)
    }

    /// Records a dynamic delete, symmetric to [`WriteTx::insert_dyn`]
    /// (write-context interning, as [`WriteTx::delete`]).
    ///
    /// # Errors
    ///
    /// As [`WriteTx::insert_dyn`].
    pub fn delete_dyn(&mut self, rel: RelationId, values: &[Value]) -> Result<bool> {
        self.encode_dyn(rel, values)?;
        self.delta.delete(&self.view, rel, &self.scratch)
    }

    /// Encodes a dynamic fact into `self.scratch`, interning through the
    /// delta. Shape problems are typed errors — ETL input is data
    /// (`docs/architecture/60-api.md`).
    fn encode_dyn(&mut self, rel: RelationId, values: &[Value]) -> Result<()> {
        let relation = self.schema.relation(rel);
        let fields = relation.fields();
        if values.len() != fields.len() {
            return Err(FactShapeError::ArityMismatch {
                relation: rel,
                expected: fields.len(),
                supplied: values.len(),
            }
            .into());
        }
        let mut refs = std::mem::take(&mut self.refs);
        refs.clear();
        for (idx, (value, field)) in values.iter().zip(fields).enumerate() {
            let field_id = FieldId(u16::try_from(idx).expect("validated schema: fields fit u16"));
            if let Err(mismatch) = crate::ir::value_matches(value, &field.value_type) {
                self.refs = refs; // restore the scratch before erroring
                return Err(match mismatch {
                    crate::ir::ValueMismatch::Type => FactShapeError::TypeMismatch {
                        relation: rel,
                        field: field_id,
                    },
                    crate::ir::ValueMismatch::EnumOrdinal(ordinal) => {
                        FactShapeError::EnumOrdinalOutOfRange {
                            relation: rel,
                            field: field_id,
                            ordinal,
                        }
                    }
                    crate::ir::ValueMismatch::Utf8 => FactShapeError::InvalidUtf8 {
                        relation: rel,
                        field: field_id,
                    },
                }
                .into());
            }
            let value_ref = match value {
                Value::Bool(v) => ValueRef::Bool(*v),
                Value::U64(v) => ValueRef::U64(*v),
                Value::I64(v) => ValueRef::I64(*v),
                Value::Enum(ordinal) => ValueRef::Enum(*ordinal),
                Value::String(raw) => {
                    let text =
                        std::str::from_utf8(raw).expect("value_matches validated UTF-8 above");
                    match self.delta.intern_str(&self.view, text) {
                        Ok(id) => ValueRef::String(id),
                        Err(err) => {
                            self.refs = refs;
                            return Err(err);
                        }
                    }
                }
                Value::Bytes(raw) => match self.delta.intern_bytes(&self.view, raw) {
                    Ok(id) => ValueRef::Bytes(id),
                    Err(err) => {
                        self.refs = refs;
                        return Err(err);
                    }
                },
            };
            refs.push(value_ref);
        }
        self.scratch.clear();
        encode_fact(&refs, relation.layout(), &mut self.scratch);
        self.refs = refs;
        Ok(())
    }
}

/// `schema!` expansion plumbing: the generated `Fact` impls call these.
/// Not API — no stability promises; nothing here is reachable from the
/// documented surface.
#[doc(hidden)]
pub mod plumbing {
    use super::{decode_field, dict, RelationId, Snapshot, ValueRef, WriteTx};
    use crate::error::{CorruptionError, Error, Result};

    /// Write-context interning: novel values mint provisional ids flushed
    /// at commit.
    ///
    /// # Errors
    ///
    /// Storage errors from the dictionary reads.
    pub fn intern_str_write(tx: &mut WriteTx<'_>, value: &str) -> Result<u64> {
        tx.delta.intern_str(&tx.view, value)
    }

    /// Write-context interning for bytes.
    ///
    /// # Errors
    ///
    /// Storage errors from the dictionary reads.
    pub fn intern_bytes_write(tx: &mut WriteTx<'_>, value: &[u8]) -> Result<u64> {
        tx.delta.intern_bytes(&tx.view, value)
    }

    /// Read-context lookup: `None` means never interned — the fact cannot
    /// exist.
    ///
    /// # Errors
    ///
    /// Storage errors from the dictionary reads.
    pub fn intern_str_read(snap: &Snapshot<'_>, value: &str) -> Result<Option<u64>> {
        dict::lookup_str(&snap.txn, value)
    }

    /// Read-context lookup for bytes.
    ///
    /// # Errors
    ///
    /// Storage errors from the dictionary reads.
    pub fn intern_bytes_read(snap: &Snapshot<'_>, value: &[u8]) -> Result<Option<u64>> {
        dict::lookup_bytes(&snap.txn, value)
    }

    /// Resolves an intern id to an owned `String` (decode boundary).
    ///
    /// # Errors
    ///
    /// `Corruption` on a dangling id or non-UTF-8 stored bytes.
    pub fn resolve_string(snap: &Snapshot<'_>, id: u64) -> Result<String> {
        let raw = dict::resolve(&snap.txn, id, dict::TAG_STRING)?;
        String::from_utf8(raw.to_vec())
            .map_err(|_| Error::Corruption(CorruptionError::NonUtf8Intern(id)))
    }

    /// Resolves an intern id to owned bytes (decode boundary).
    ///
    /// # Errors
    ///
    /// `Corruption` on a dangling id.
    pub fn resolve_bytes(snap: &Snapshot<'_>, id: u64) -> Result<Vec<u8>> {
        Ok(dict::resolve(&snap.txn, id, dict::TAG_BYTES)?.to_vec())
    }

    /// Appends the canonical fact bytes for a write-context encode.
    pub fn encode_write_fact(
        tx: &WriteTx<'_>,
        rel: RelationId,
        values: &[ValueRef],
        out: &mut Vec<u8>,
    ) {
        crate::encoding::encode_fact(values, tx.schema.relation(rel).layout(), out);
    }

    /// Appends the canonical fact bytes for a read-context encode.
    pub fn encode_read_fact(
        snap: &Snapshot<'_>,
        rel: RelationId,
        values: &[ValueRef],
        out: &mut Vec<u8>,
    ) {
        crate::encoding::encode_fact(values, snap.schema.relation(rel).layout(), out);
    }

    /// Decodes one field of canonical fact bytes.
    ///
    /// # Errors
    ///
    /// `Corruption` on undecodable bytes.
    pub fn decode(
        snap: &Snapshot<'_>,
        rel: RelationId,
        fact: &[u8],
        idx: usize,
    ) -> Result<ValueRef> {
        Ok(decode_field(fact, snap.schema.relation(rel).layout(), idx)?)
    }
}
