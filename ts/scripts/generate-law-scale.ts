/**
 * Generates `test/fixtures/law-scale.ts` — the PRIMER-SCALE law-typing
 * probe fixture (PRD-K4's scale bar): 40 relations (8 closed vocabularies
 * + 32 ordinary relations), 200 field slots, and 123 statements, all
 * spelled INLINE in one `schema()` call so the statements tuple type stays
 * precise and the type-level class machinery computes the whole map under
 * default `tsc` limits. Deterministic output — regenerate with
 * `node scripts/generate-law-scale.ts`, never hand-edit the fixture.
 *
 * The shape mirrors the audited primer schema's proportions: generator
 * chains (`ref` containments walking the relation roster), closed-vocab
 * references (`kind` containments), cardinality windows over classed
 * slots, generator-less interval bijections (`at` mirrors — the
 * least-member/set-tier classes at scale), pointwise composite
 * containments, and σ-selected self-containments.
 */

import * as fs from "node:fs"
import * as path from "node:path"
import { fileURLToPath } from "node:url"

const RELATIONS = 32
const VOCABS = 8

function generate(): string {
	const lines: string[] = []
	lines.push("/**")
	lines.push(" * GENERATED by scripts/generate-law-scale.ts — DO NOT hand-edit; regenerate.")
	lines.push(" *")
	lines.push(" * The primer-scale law-typing fixture (PRD-K4's scale bar): 40 relations")
	lines.push(" * (8 closed + 32 ordinary), 200 field slots, 123 statements inline in one")
	lines.push(" * schema() call. Its very compilation is the probe: the type-level class")
	lines.push(" * machinery must carry this shape under default tsc limits, and the pins")
	lines.push(" * at the bottom prove the map COMPUTED (never silently widened).")
	lines.push(" */")
	lines.push("")
	lines.push('import { closed } from "#closed.ts"')
	lines.push('import { atMost } from "#count.ts"')
	lines.push('import { on } from "#face.ts"')
	lines.push('import { i64, interval, str, u64 } from "#fields.ts"')
	lines.push('import { relation } from "#relation.ts"')
	lines.push('import { schema } from "#schema.ts"')
	lines.push('import { contained, mirrors, window } from "#statements.ts"')
	lines.push("")
	lines.push("/** The identity-strength equality probe (the standard dual-function trick). */")
	lines.push("type Equal<A, B> = (<T>() => T extends A ? 1 : 2) extends <T>() => T extends B ? 1 : 2 ? true : false")
	lines.push("")
	for (let v = 0; v < VOCABS; v += 1) {
		lines.push(`const Vocab${v} = closed("Vocab${v}", ["Alpha${v}", "Beta${v}"])`)
	}
	lines.push("")
	for (let i = 0; i < RELATIONS; i += 1) {
		lines.push(
			`const R${i} = relation("R${i}", { id: u64.fresh, ref: u64, kind: u64, at: interval(u64), label: str, score: i64 })`
		)
	}
	lines.push("")
	const members: string[] = []
	for (let v = 0; v < VOCABS; v += 1) {
		members.push(`Vocab${v}`)
	}
	for (let i = 0; i < RELATIONS; i += 1) {
		members.push(`R${i}`)
	}
	const statements: string[] = []
	// 31 generator chains: R{i}.ref <= R{i-1}.id
	for (let i = 1; i < RELATIONS; i += 1) {
		statements.push(`contained(on(R${i}, "ref"), on(R${i - 1}, "id"))`)
	}
	// 32 closed-vocab references: R{i}.kind <= Vocab{i % 8}.id
	for (let i = 0; i < RELATIONS; i += 1) {
		statements.push(`contained(on(R${i}, "kind"), on(Vocab${i % VOCABS}, "id"))`)
	}
	// 20 windows over classed slots: R{i}.id groups at most 3 R{i+1}.ref
	for (let i = 0; i < 20; i += 1) {
		statements.push(`window(on(R${i}, "id"), atMost(3n), on(R${i + 1}, "ref"))`)
	}
	// 20 generator-less interval bijections: R{i}.at == R{i+1}.at
	for (let i = 0; i < 20; i += 1) {
		statements.push(`mirrors(on(R${i}, "at"), on(R${i + 1}, "at"))`)
	}
	// 11 pointwise composite containments: R{i}(ref, at) <= R{i-1}(id, at)
	for (let i = 21; i < RELATIONS; i += 1) {
		statements.push(`contained(on(R${i}, ["ref", "at"]), on(R${i - 1}, ["id", "at"]))`)
	}
	// 9 σ-selected self-containments: R{i}(id | label == "hot") <= R{i}(id)
	for (let i = 0; i < 9; i += 1) {
		statements.push(`contained(on(R${i}.where({ label: "hot" }), "id"), on(R${i}, "id"))`)
	}
	lines.push(`const LawScale = schema("LawScale", { ${members.join(", ")} }, [`)
	lines.push(statements.map((statement) => `\t${statement}`).join(",\n"))
	lines.push("])")
	lines.push("")
	lines.push("// The machinery COMPUTED (never silently widened) — spot pins across the laws.")
	lines.push('const pinChain: Equal<(typeof LawScale)["classes"]["R5"]["ref"], "R4.id"> = true')
	lines.push('const pinVocab: Equal<(typeof LawScale)["classes"]["R9"]["kind"], "Vocab1.id"> = true')
	lines.push('const pinGenerator: Equal<(typeof LawScale)["classes"]["R31"]["id"], "R31.id"> = true')
	lines.push('const pinClosed: Equal<(typeof LawScale)["classes"]["Vocab7"]["id"], "Vocab7.id"> = true')
	lines.push('const pinBare: Equal<(typeof LawScale)["classes"]["R3"]["score"], undefined> = true')
	lines.push("")
	lines.push("export { LawScale, pinBare, pinChain, pinClosed, pinGenerator, pinVocab }")
	lines.push("")
	return lines.join("\n")
}

const outPath = path.join(fileURLToPath(new URL("..", import.meta.url)), "test", "fixtures", "law-scale.ts")
fs.writeFileSync(outPath, generate())
console.log(`law-scale fixture written: ${outPath}`)
